class TestWhile{
	
	//aqui utilizo o comando while(enquanto) para fazer um loop
		public static void main(String[] args){
		int idade = 15;
	//enquanto idade nao for igual ou for menor que 18 fazer loop adicionando +1
		while (idade <= 18) {
		System.out.println(idade);
		idade = idade + 1;
		
		}

	}
}